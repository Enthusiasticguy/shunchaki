from turtle import *
# uni 5 tezlikda chizishini aytamiz
speed(5)
# orqa fonni qora rangda qilishni aytamiz
bgcolor("black")
# yashil rangda chizishini aytamiz
color("green")
# i degan o'zgaruvchi yaratamiz
i=200
while i>0:
  # sikl to'xtaguncha chapga yuradi
  left(i)
  # sikl to'xtaguncha oldinga yuradi
  forward(i*3)
  # i o'zgaruvchini bittaga kamaytiramiz
  i=i-1
# va bajaramiz
done()  
  
  
  
